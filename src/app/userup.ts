export class Userup {
    id?:any;
    name?:any;
    email?:any;
    gender?:any;
    dob?:any;
    username?:any;
    password?:any;

    constructor(id?:any, name?:any, email?:any, gender?:any, dob?:any, username?:any, password?:any) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.gender = gender;
        this.dob = dob;
        this.username = username;
        this.password = password;
    }
}
